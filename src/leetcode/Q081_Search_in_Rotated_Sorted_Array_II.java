package leetcode;

class Solution81 {
    public boolean search(int[] nums, int target) {
        if (nums.length==0) return false;
        return search(nums, target, 0, nums.length-1);
    }
    
    private boolean search(int[] nums, int target, int l, int r) {
        
        while (nums[l]==nums[r]) {
            l++;
        }
        
        int mid = (l+r)/2;
        if (nums[mid]==target) return true;
        if (l>=r) return false;
        
        boolean res = false;
        
        
        if (l<=mid-1 && nums[l]<=nums[mid-1]) {
            if (nums[l]<=target && target<=nums[mid-1]) {
                res = search(nums, target, l, mid-1);
            }
            else {
                res = search(nums, target, mid+1, r);
            }
        }
        else {  // nums[mid+1]<=nums[r]
            if (nums[mid+1]<=target && target<=nums[r]) {
                res = search(nums, target, mid+1, r);
            }
            else {
                res = search(nums, target, l, mid-1);
            }
        }
        
        return res;
    }
}

